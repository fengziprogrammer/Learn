class Demo5 {

	/*
		输出的内容"Hello World!"
		Java语法中有些符号 特定含义的
		""
		使用希望 不再是原来的含义
		转换含义

		需要使用转义字符\
	*/
	public static void main(String[] args) {

		System.out.println("\"Hello World!\"");
		System.out.println("tHello World!t");
		System.out.println("\tHello World!\t");
		System.out.println('\\');
		System.out.println("hello\tworld\njava");
		System.out.println("tHello World!t");
		System.out.println("\r\n");
		System.out.println("tHello World!t");
	}
}
