class Demo3{

/*
		浮点类
			float  单精度  4个字节
			double 双精度  8个字节  默认

			仅用 数值的存储
			不推荐做运算
			float  标记 f /F
*/
	public static void main(String[] args) {


		double d1 = 2.0;
		double d2 = 1.1;
		double d3 = d1 - d2;

		System.out.println(d1);
		System.out.println(d2);
		System.out.println(d3);

		float f1 = 3.1f;
		System.out.println(f1);

	}
}
