package string;

/**
 * String toLowerCase()
 * String toUpperCase()
 * 将字符串中的英文部分转换为全大写或全小写
 */
public class ToUpperCaseDemo {
    public static void main(String[] args) {
        String str = "我爱Java";
        String lower = str.toLowerCase();
        System.out.println(lower);

        String upper = str.toUpperCase();
        System.out.println(upper);
    }
}



